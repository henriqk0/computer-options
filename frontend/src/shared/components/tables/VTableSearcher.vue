<template>
  <div class="relative mt-4">
    <svg
      class="absolute left-3 top-2.5 w-5 h-5 text-gray-400 dark:text-neutral-500"
      fill="none"
      stroke="currentColor"
      viewBox="0 0 24 24"
    >
      <path
        stroke-linecap="round"
        stroke-linejoin="round"
        stroke-width="2"
        d="M21 21l-6-6m2-5a7 7 0 11-14 0 7 7 0 0114 0z"
      ></path>
    </svg>
    <input
      v-model="searcher"
      type="text"
      @keydown.enter="sendSearchEvent"
      class="w-full md:w-100 pl-10 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-transparent dark:text-neutral-100 dark:placeholder-neutral-500 dark:border-neutral-700 dark:bg-neutral-800"
      :placeholder="searcherPlaceholder"
    />
  </div>
</template>

<script setup lang="ts">
import { ref } from 'vue'

const props = defineProps<{
  searchableFields: string[]
}>()

const searcher = ref('')

const searcherPlaceholder = ref('Buscar por ' + props.searchableFields.join(', ') + '...')

const emit = defineEmits(['toSearch'])

function sendSearchEvent() {
  emit('toSearch', searcher.value)
}
</script>
